package com.handong.oh318;

public class Point {
    
    private float x ; 
    private float y ; 

    public void setX(float x) { 
        this.x = x; 
    }

    public void setY(float y) { 
        this.y = y ;
    }

    public float getX() { 
        return x ;
    }
    
    public float getY() { 
        return y ;
    }
}
